export async function analyzeTicketEvidence(payload: {
  ticketId: string;
  adminUid: string;
  adminName: string;
}) {
  const database = getDb();
  const ticketSnapshot = await getDoc(doc(database, "supportTickets", payload.ticketId));
  if (!ticketSnapshot.exists()) throw new Error("Ticket not found.");
  const ticket = mapSupportTicket(ticketSnapshot.id, ticketSnapshot.data());
  const evidenceCount = ticket.evidenceUrls.length;
  const descriptionLength = ticket.description.trim().length;
  const expectedLength = ticket.expectedOutcome.trim().length;
  const keywordSignals = /(invoice|receipt|order|payment|chat|screenshot|video|email)/i.test(
    `${ticket.description} ${ticket.expectedOutcome}`,
  )
    ? 1
    : 0;
  const priorityWeight = ticket.priority === "critical" ? 12 : ticket.priority === "high" ? 8 : 4;
  const evidenceScore = Math.max(
    0,
    Math.min(
      100,
      Math.round(
        evidenceCount * 22 +
          Math.min(24, Math.round(descriptionLength / 8)) +
          Math.min(12, Math.round(expectedLength / 10)) +
          keywordSignals * 8 +
          priorityWeight,
      ),
    ),
  );
  const confidence: TicketEvidenceAssessmentRecord["confidence"] =
    evidenceScore >= 75 ? "high" : evidenceScore >= 45 ? "medium" : "low";
  const riskFlags: string[] = [];
  if (evidenceCount === 0) riskFlags.push("no_evidence_uploaded");
  if (descriptionLength < 40) riskFlags.push("low_context_description");
  if (expectedLength < 12) riskFlags.push("expected_outcome_not_clear");
  if (ticket.priority === "critical" && evidenceCount <= 1) {
    riskFlags.push("critical_without_strong_proof");
  }
  const recommendation =
    confidence === "high"
      ? "Evidence is strong. Proceed to direct mediation and likely resolution."
      : confidence === "medium"
        ? "Request one additional proof artifact before final resolution."
        : "Insufficient evidence. Ask customer for invoice/payment proof and timeline details.";

  const ref = doc(database, "ticketEvidenceAssessments", payload.ticketId);
  await setDoc(
    ref,
    {
      ticketId: ticket.id,
      businessName: ticket.businessName,
      customerEmail: ticket.customerEmail,
      evidenceScore,
      confidence,
      riskFlags,
      recommendation,
      assessedByUid: payload.adminUid,
      assessedByName: payload.adminName,
      createdAt: serverTimestamp(),
      updatedAt: serverTimestamp(),
    },
    { merge: true },
  );
}

export async function fetchTicketEvidenceAssessments(limitRows = 120) {
  const database = getDb();
  const snapshots = await getDocs(
    query(
      collection(database, "ticketEvidenceAssessments"),
      orderBy("updatedAt", "desc"),
      limit(Math.max(1, Math.min(300, Math.round(limitRows)))),
    ),
  );
  return snapshots.docs.map((snapshot) => mapEvidenceAssessment(snapshot.id, snapshot.data()));
}

