type TimestampValue = { toDate: () => Date };

function toISODate(value: unknown) {
  const maybeTimestamp = value as TimestampValue | undefined;
  if (maybeTimestamp && typeof maybeTimestamp.toDate === "function") {
    return maybeTimestamp.toDate().toISOString();
  }
  if (typeof value === "string" && value.trim()) return value;
  return new Date().toISOString();
}

function getDb() {
  if (!db) {
    throw new Error("Firebase is not configured. Add NEXT_PUBLIC_FIREBASE_* values.");
  }
  return db;
}

function normalizeSlug(text: string) {
  return text
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function randomFragment(length = 8) {
  return Math.random().toString(36).slice(2, 2 + length);
}

type BasicBusinessRecord = {
  id: string;
  slug: string;
  ownerUid: string;
  businessName: string;
  status: string;
};

function mapBasicBusiness(snapshotId: string, data: Record<string, unknown>): BasicBusinessRecord {
  return {
    id: snapshotId,
    slug: String(data.slug ?? normalizeSlug(String(data.businessName ?? snapshotId))),
    ownerUid: String(data.ownerUid ?? ""),
    businessName: String(data.businessName ?? "Business"),
    status: String(data.status ?? "pending"),
  };
}

async function fetchPrimaryBusinessByOwner(ownerUid: string) {
  const database = getDb();
  const approvedSnapshots = await getDocs(
    query(
      collection(database, "businessApplications"),
      where("ownerUid", "==", ownerUid),
      where("status", "==", "approved"),
      limit(20),
    ),
  );
  if (!approvedSnapshots.empty) {
    const row = approvedSnapshots.docs[0];
    return mapBasicBusiness(row.id, row.data());
  }
  const fallbackSnapshots = await getDocs(
    query(
      collection(database, "businessApplications"),
      where("ownerUid", "==", ownerUid),
      limit(20),
    ),
  );
  if (fallbackSnapshots.empty) return null;
  const row = fallbackSnapshots.docs[0];
  return mapBasicBusiness(row.id, row.data());
}

async function fetchBusinessBySlug(slug: string) {
  const cleanSlug = normalizeSlug(slug);
  if (!cleanSlug) return null;
  const database = getDb();
  const snapshots = await getDocs(
    query(
      collection(database, "businessApplications"),
      where("slug", "==", cleanSlug),
      limit(1),
    ),
  );
  const row = snapshots.docs[0];
  return row ? mapBasicBusiness(row.id, row.data()) : null;
}

