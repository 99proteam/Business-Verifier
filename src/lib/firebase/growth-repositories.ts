export type VerificationTierKey = "basic" | "advanced" | "pro_escrow";
export type VerificationTierStatus = "pending" | "approved" | "rejected";

export interface BusinessVerificationTierRecord {
  id: string;
  businessId: string;
  businessSlug: string;
  businessName: string;
  ownerUid: string;
  requestedTier: VerificationTierKey;
  currentTier: VerificationTierKey;
  status: VerificationTierStatus;
  requestedNote?: string;
  adminReason?: string;
  reviewedByUid?: string;
  reviewedByName?: string;
  requestedAt: string;
  updatedAt: string;
}

function mapVerificationTier(snapshotId: string, data: Record<string, unknown>) {
  const requestedTier = String(data.requestedTier ?? "basic");
  const currentTier = String(data.currentTier ?? "basic");
  const toTier = (value: string): VerificationTierKey =>
    value === "advanced" || value === "pro_escrow" ? value : "basic";
  const status = String(data.status ?? "pending");
  return {
    id: snapshotId,
    businessId: String(data.businessId ?? ""),
    businessSlug: String(data.businessSlug ?? ""),
    businessName: String(data.businessName ?? "Business"),
    ownerUid: String(data.ownerUid ?? ""),
    requestedTier: toTier(requestedTier),
    currentTier: toTier(currentTier),
    status: status === "approved" || status === "rejected" ? status : "pending",
    requestedNote: data.requestedNote ? String(data.requestedNote) : undefined,
    adminReason: data.adminReason ? String(data.adminReason) : undefined,
    reviewedByUid: data.reviewedByUid ? String(data.reviewedByUid) : undefined,
    reviewedByName: data.reviewedByName ? String(data.reviewedByName) : undefined,
    requestedAt: toISODate(data.requestedAt),
    updatedAt: toISODate(data.updatedAt),
  } satisfies BusinessVerificationTierRecord;
}

export async function fetchBusinessVerificationTierByOwner(ownerUid: string) {
  const business = await fetchPrimaryBusinessByOwner(ownerUid);
  if (!business) return null;
  const database = getDb();
  const snapshot = await getDoc(doc(database, "businessVerificationTiers", business.id));
  if (!snapshot.exists()) {
    return {
      id: business.id,
      businessId: business.id,
      businessSlug: business.slug,
      businessName: business.businessName,
      ownerUid,
      requestedTier: "basic",
      currentTier: "basic",
      status: "approved",
      requestedAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    } satisfies BusinessVerificationTierRecord;
  }
  return mapVerificationTier(snapshot.id, snapshot.data());
}

export async function fetchBusinessVerificationTierBySlug(slug: string) {
  const business = await fetchBusinessBySlug(slug);
  if (!business) return null;
  const database = getDb();
  const snapshot = await getDoc(doc(database, "businessVerificationTiers", business.id));
  if (!snapshot.exists()) return null;
  return mapVerificationTier(snapshot.id, snapshot.data());
}

export async function requestBusinessVerificationTier(payload: {
  ownerUid: string;
  requestedTier: VerificationTierKey;
  note?: string;
}) {
  const business = await fetchPrimaryBusinessByOwner(payload.ownerUid);
  if (!business) throw new Error("Business profile not found.");
  const database = getDb();
  const ref = doc(database, "businessVerificationTiers", business.id);
  const existing = await getDoc(ref);
  const current = existing.exists()
    ? mapVerificationTier(existing.id, existing.data())
    : null;
  await setDoc(
    ref,
    {
      businessId: business.id,
      businessSlug: business.slug,
      businessName: business.businessName,
      ownerUid: payload.ownerUid,
      requestedTier: payload.requestedTier,
      currentTier: current?.currentTier ?? "basic",
      status: "pending",
      requestedNote: payload.note?.trim() || null,
      requestedAt: serverTimestamp(),
      updatedAt: serverTimestamp(),
    },
    { merge: true },
  );
}

