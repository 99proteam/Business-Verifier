export async function refreshMerchantRiskProfile(ownerUid: string) {
  const business = await fetchPrimaryBusinessByOwner(ownerUid);
  if (!business) throw new Error("Business not found.");
  const database = getDb();
  const [orderSnapshots, ticketSnapshots] = await Promise.all([
    getDocs(
      query(collection(database, "orders"), where("businessOwnerUid", "==", ownerUid), limit(600)),
    ),
    getDocs(
      query(
        collection(database, "supportTickets"),
        where("participantUids", "array-contains", ownerUid),
        limit(400),
      ),
    ),
  ]);
  const orders = orderSnapshots.docs.map((snapshot) =>
    mapOrder(snapshot.id, snapshot.data() as Record<string, unknown>),
  );
  const tickets = ticketSnapshots.docs.map((snapshot) =>
    mapSupportTicket(snapshot.id, snapshot.data()),
  );
  const totalOrders = orders.length;
  const refundedOrders = orders.filter((row) => row.status === "refunded").length;
  const openTickets = tickets.filter((row) =>
    ["open", "in_discussion", "awaiting_admin"].includes(row.status),
  ).length;
  const refundRatePercent =
    totalOrders > 0 ? Number(((refundedOrders / totalOrders) * 100).toFixed(2)) : 0;
  const ticketRatePercent = totalOrders > 0 ? (openTickets / totalOrders) * 100 : openTickets * 5;
  const riskScore = Math.max(
    0,
    Math.min(100, Math.round(refundRatePercent * 1.6 + ticketRatePercent * 1.3)),
  );
  const riskBand = determineRiskBand(riskScore);
  await setDoc(
    doc(database, "merchantRiskProfiles", business.id),
    {
      businessId: business.id,
      businessName: business.businessName,
      businessSlug: business.slug,
      ownerUid,
      totalOrders,
      refundedOrders,
      refundRatePercent,
      openTickets,
      riskScore,
      riskBand,
      updatedAt: serverTimestamp(),
    },
    { merge: true },
  );
}

