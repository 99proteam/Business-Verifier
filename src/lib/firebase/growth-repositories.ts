export interface WidgetConversionAnalyticsRecord {
  ownerUid: string;
  businessId: string;
  businessName: string;
  totalImpressions: number;
  totalClicks: number;
  totalOrders: number;
  ctrPercent: number;
  clickToOrderPercent: number;
  daily: WidgetConversionDailyRecord[];
}

export async function fetchWidgetConversionAnalyticsByOwner(ownerUid: string, days = 30) {
  const business = await fetchPrimaryBusinessByOwner(ownerUid);
  if (!business) {
    return null;
  }
  const database = getDb();
  const since = new Date(Date.now() - Math.max(1, days) * 24 * 60 * 60 * 1000);
  const [widgetSnapshots, orderSnapshots] = await Promise.all([
    getDocs(
      query(
        collection(database, "trustBadgeWidgetDailyStats"),
        where("ownerUid", "==", ownerUid),
        orderBy("dateKey", "desc"),
        limit(Math.max(1, days + 7)),
      ),
    ),
    getDocs(
      query(
        collection(database, "orders"),
        where("businessOwnerUid", "==", ownerUid),
        orderBy("createdAt", "desc"),
        limit(700),
      ),
    ),
  ]);
  const orders = orderSnapshots.docs
    .map((snapshot) => mapOrder(snapshot.id, snapshot.data() as Record<string, unknown>))
    .filter((row) => Date.parse(row.createdAt) >= since.getTime());

  const ordersByDate = new Map<string, number>();
  for (const row of orders) {
    const key = row.createdAt.slice(0, 10);
    ordersByDate.set(key, (ordersByDate.get(key) ?? 0) + 1);
  }

  const daily: WidgetConversionDailyRecord[] = widgetSnapshots.docs
    .map((snapshot) => {
      const data = snapshot.data() as Record<string, unknown>;
      const dateKey = String(data.dateKey ?? "");
      const impressions = Number(data.impressions ?? 0);
      const clicks = Number(data.clicks ?? 0);
      const dayOrders = ordersByDate.get(dateKey) ?? 0;
      return {
        dateKey,
        impressions,
        clicks,
        orders: dayOrders,
        conversionRatePercent:
          clicks > 0 ? Number(((dayOrders / clicks) * 100).toFixed(2)) : 0,
      } satisfies WidgetConversionDailyRecord;
    })
    .filter((row) => row.dateKey && Date.parse(`${row.dateKey}T00:00:00.000Z`) >= since.getTime())
    .sort((a, b) => Date.parse(b.dateKey) - Date.parse(a.dateKey));

  const totalImpressions = daily.reduce((sum, row) => sum + row.impressions, 0);
  const totalClicks = daily.reduce((sum, row) => sum + row.clicks, 0);
  const totalOrders = daily.reduce((sum, row) => sum + row.orders, 0);
  const ctrPercent = totalImpressions > 0 ? Number(((totalClicks / totalImpressions) * 100).toFixed(2)) : 0;
  const clickToOrderPercent = totalClicks > 0 ? Number(((totalOrders / totalClicks) * 100).toFixed(2)) : 0;

  const snapshotId = `${business.id}_${new Date().toISOString().slice(0, 7)}`;
  await setDoc(
    doc(database, "widgetConversionSnapshots", snapshotId),
    {
      ownerUid,
      businessId: business.id,
      businessName: business.businessName,
      totalImpressions,
      totalClicks,
      totalOrders,
      ctrPercent,
      clickToOrderPercent,
      updatedAt: serverTimestamp(),
    },
    { merge: true },
  );

  return {
    ownerUid,
    businessId: business.id,
    businessName: business.businessName,
    totalImpressions,
    totalClicks,
    totalOrders,
    ctrPercent,
    clickToOrderPercent,
    daily,
  } satisfies WidgetConversionAnalyticsRecord;
}

