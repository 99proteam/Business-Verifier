export async function evaluateDisputeSlaBreaches(adminUid: string, limitRows = 160) {
  const database = getDb();
  const policy = await fetchDisputeSlaPolicy();
  const tickets = await fetchOpenDisputeTickets(limitRows);
  const now = Date.now();
  const breaches: DisputeSlaBreachRecord[] = [];
  for (const ticket of tickets) {
    const ageHours = Math.max(
      0,
      Number(((now - Date.parse(ticket.createdAt)) / (60 * 60 * 1000)).toFixed(1)),
    );
    let breachStage: DisputeSlaBreachRecord["breachStage"] = "none";
    if (ageHours >= policy.autoRefundHours) {
      breachStage = "auto_refund_due";
    } else if (ageHours >= policy.adminEscalationHours) {
      breachStage = "admin_escalation_due";
    } else if (ageHours >= policy.firstResponseHours) {
      breachStage = "response_due";
    }
    if (breachStage === "none") continue;
    const breachRef = doc(database, "disputeSlaBreaches", ticket.id);
    await setDoc(
      breachRef,
      {
        ticketId: ticket.id,
        businessName: ticket.businessName,
        customerEmail: ticket.customerEmail,
        ownerUid: ticket.participantUids.find((uid) => uid !== ticket.customerUid) ?? null,
        status: ticket.status,
        ageHours,
        breachStage,
        reviewedByUid: adminUid,
        createdAt: serverTimestamp(),
      },
      { merge: true },
    );
    breaches.push({
      id: ticket.id,
      ticketId: ticket.id,
      businessName: ticket.businessName,
      customerEmail: ticket.customerEmail,
      status: ticket.status,
      ageHours,
      breachStage,
      ownerUid: ticket.participantUids.find((uid) => uid !== ticket.customerUid),
      createdAt: new Date().toISOString(),
    });
  }
  return breaches;
}

export async function fetchDisputeSlaBreaches(limitRows = 120) {
  const database = getDb();
  const snapshots = await getDocs(
    query(
      collection(database, "disputeSlaBreaches"),
      orderBy("createdAt", "desc"),
      limit(Math.max(1, Math.min(300, Math.round(limitRows)))),
    ),
  );
  return snapshots.docs.map((snapshot) =>
    mapDisputeSlaBreach(snapshot.id, snapshot.data()),
  );
}

